    private void btnValidarFirmaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValidarFirmaActionPerformed
        
        JFileChooser filechooser = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
        filechooser.setDialogTitle("Seleccione archivo a verificar");
         int return_value = filechooser.showOpenDialog(null);
         if(return_value == JFileChooser.APPROVE_OPTION){
             File selectedFile = filechooser.getSelectedFile();
             JFileChooser filechooserfirma = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
            filechooserfirma.setDialogTitle("Seleccione firma del archivo");
            int return_value_firma = filechooserfirma.showOpenDialog(null);
            if(return_value_firma == JFileChooser.APPROVE_OPTION){
                File selectedFileFirma = filechooserfirma.getSelectedFile();
                String ci = JOptionPane.showInputDialog("Ingrese ci del autor");
                if(CipherController.verificar(selectedFile, selectedFileFirma, ci)){
                    
                    JOptionPane.showMessageDialog(this, "La firma es v√°lida");
                }
                else{
                    JOptionPane.showMessageDialog(this, "La firma no es v√°lida");
                }
            }
             
         }
    }//GEN-LAST:event_btnValidarFirmaActionPerformed

    private void btnModificarContraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarContraActionPerformed
        String claveActual = JOptionPane.showInputDialog("Ingrese la contrase√±a actual");
        String claveNueva = JOptionPane.showInputDialog("Ingrese una nueva contrase√±a");
        String claveNueva2 = JOptionPane.showInputDialog("Repita la nueva contrase√±a");
        
        
        if(claveNueva.equals(claveNueva2)){
            UsersController us = new UsersController();
            try{
                boolean resultado = us.modificarContrasena(usuario, claveNueva, claveActual);
                if(resultado){
                    this.actualizar();
                    JOptionPane.showMessageDialog(this, "Se modific√≥ la contrase√±a con √©xito ");
                }
                else{
                    JOptionPane.showMessageDialog(this, "No se ha podido modificar la contrase√±a con √©xito ");
                    
                }
            }
            catch(ContrasenaIncorrectaException e){
                JOptionPane.showMessageDialog(this, "La contrase√±a actual ingresada no es correcta");
            }
        }
        else {
            JOptionPane.showMessageDialog(this, "La nueva contrase√±a no concuerda con la repetici√≥n");
        }
    }//GEN-LAST:event_btnModificarContraActionPerformed

