    private void btnFirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFirmarActionPerformed
          JFileChooser filechooser = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
          filechooser.setDialogTitle("Seleccione archivo a firmar");
          int return_value = filechooser.showOpenDialog(null);
          if(return_value == JFileChooser.APPROVE_OPTION){
              File selectedFile = filechooser.getSelectedFile();
              
              JFileChooser filechooserkey = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
              filechooserkey.setDialogTitle("Seleccione PrivateKey");
              int return_key_value = filechooserkey.showOpenDialog(null);
              if(return_key_value == JFileChooser.APPROVE_OPTION){
                File selectedKey = filechooserkey.getSelectedFile();
                if(CipherController.firmado(selectedFile,selectedKey)){
                    JOptionPane.showMessageDialog(this, "Se logr√≥ firmar el archivo correctamente");   
                }
                else{
                    JOptionPane.showMessageDialog(this, "No se logr√≥ firmar el archivo correctamente");   
                    
                }
              }
          }
    }//GEN-LAST:event_btnFirmarActionPerformed

    private void btnCifradoSimetricoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCifradoSimetricoActionPerformed
        //Elegimos el archivo a encriptar
        JFileChooser filechooser = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
        int return_value = filechooser.showOpenDialog(null);
        if(return_value == JFileChooser.APPROVE_OPTION){
            //Solicitamos la clave de encriptaci√≥n
            String clave = JOptionPane.showInputDialog("Ingrese la clave de cifrado");
            //Solicitamos un nuevo nombre para el archivo encriptado
            String nuevoNombre = JOptionPane.showInputDialog("Ingrese un nombre distinto para el nuevo archivo");
            File selectedFile = filechooser.getSelectedFile();
            
            boolean resultado = CipherController.cifradoSimetrico(selectedFile, clave, nuevoNombre);
            if(resultado){
                System.out.println("Se logr√≥ cifrar el archivo correctamente");
                JOptionPane.showMessageDialog(this, "Se logr√≥ cifrar el archivo correctamente");

            } else{
                System.out.println("No se logr√≥ cifrar el archivo");
                JOptionPane.showMessageDialog(this, "No se logr√≥ cifrar el archivo");

            }
        }                           
    }//GEN-LAST:event_btnCifradoSimetricoActionPerformed

